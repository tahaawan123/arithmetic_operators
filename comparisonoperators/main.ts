// Comparison operators
let a =10;
let b =12;
let c ="10";
let d =30;
let e  =12;
// (==) Equality Operator compares two values for equality.
console.log(b == e);
// (===) Strict Equality Operator mean that  both the value and the type of the operand must be the same
console.log(b === e);
// if either the value or the type of the operand is  different the Strict Equality operator will return false
console.log(a === c);
// (>)Greater Than OPerator 
// returns (True) if the left operand is greater than the right operand.
console.log(b > a);
// (>=) Greter Than OR Equal to 
console.log(d >= e);
console.log(e >= b);
//   (<) Less Than 
console.log(b < d);
// (<=)Less Than OR Equal to
console.log(b <= e);
console.log(b <= d);
// (!=) Not equal to
console.log(d != e);
